package cbsq.riffmt.ebmls













/**
 * 
 * the limited set of the "charset"s 
 * introduced to base the unification in `EBml.Schema`
 * 
 * https://datatracker.ietf.org/doc/rfc8794/ 
 * 
 */
enum SpecialCharset {

   case Utf8
   case AsciiString
   case RawOctetString
   
   case EBmlBigInt
   
}















